/**
 * Severity of a {@link FieldViolation}. {@code ERROR} blocks the write (HTTP 422 / MCP refusal);
 * {@code WARNING} is advisory — the write proceeds and the warning is returned for display.
 */
public enum Severity {
    ERROR,
    WARNING
}
