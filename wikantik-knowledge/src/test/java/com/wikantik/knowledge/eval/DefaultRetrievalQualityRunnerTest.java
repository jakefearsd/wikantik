package com.wikantik.knowledge.eval;

import com.wikantik.api.eval.RetrievalMode;
import com.wikantik.api.eval.RetrievalRunResult;
import org.h2.jdbcx.JdbcDataSource;
import org.junit.jupiter.api.AfterEach;
import org.junit.jupiter.api.BeforeEach;
import org.junit.jupiter.api.Test;

import javax.sql.DataSource;
import java.sql.Connection;
import java.sql.Statement;
import java.time.Instant;
import java.util.List;
import java.util.Map;
import java.util.Optional;
import java.util.concurrent.atomic.AtomicInteger;

import static org.junit.jupiter.api.Assertions.assertDoesNotThrow;
import static org.junit.jupiter.api.Assertions.assertEquals;
import static org.junit.jupiter.api.Assertions.assertFalse;
import static org.junit.jupiter.api.Assertions.assertNotEquals;
import static org.junit.jupiter.api.Assertions.assertNotNull;
import static org.junit.jupiter.api.Assertions.assertNull;
import static org.junit.jupiter.api.Assertions.assertThrows;
import static org.junit.jupiter.api.Assertions.assertTrue;

class DefaultRetrievalQualityRunnerTest {

    private DataSource ds;
    private RetrievalQualityDao dao;
    private RetrievalQualityMetrics metrics;

    @BeforeEach
    void setUp() throws Exception {
        final JdbcDataSource h2 = new JdbcDataSource();
        h2.setURL( "jdbc:h2:mem:rqr;MODE=PostgreSQL;DB_CLOSE_DELAY=-1" );
        this.ds = h2;
        try ( Connection c = ds.getConnection(); Statement s = c.createStatement() ) {
            s.executeUpdate( """
                CREATE TABLE retrieval_query_sets (
                    id VARCHAR(64) PRIMARY KEY,
                    name VARCHAR(128) NOT NULL,
                    description TEXT,
                    created_at TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP
                )""" );
            s.executeUpdate( """
                CREATE TABLE retrieval_queries (
                    query_set_id VARCHAR(64) NOT NULL REFERENCES retrieval_query_sets(id),
                    query_id     VARCHAR(64) NOT NULL,
                    query_text   TEXT NOT NULL,
                    expected_ids VARCHAR(64) ARRAY NOT NULL,
                    PRIMARY KEY (query_set_id, query_id)
                )""" );
            s.executeUpdate( """
                CREATE TABLE retrieval_runs (
                    run_id BIGINT GENERATED BY DEFAULT AS IDENTITY PRIMARY KEY,
                    query_set_id VARCHAR(64) NOT NULL REFERENCES retrieval_query_sets(id),
                    started_at   TIMESTAMP WITH TIME ZONE NOT NULL DEFAULT CURRENT_TIMESTAMP,
                    finished_at  TIMESTAMP WITH TIME ZONE,
                    mode         VARCHAR(32) NOT NULL,
                    ndcg_at_5    NUMERIC(5,4),
                    ndcg_at_10   NUMERIC(5,4),
                    recall_at_20 NUMERIC(5,4),
                    mrr          NUMERIC(5,4),
                    notes        TEXT
                )""" );
            s.executeUpdate( "INSERT INTO retrieval_query_sets (id, name) VALUES ('core', 'Core')" );
            // q1 expected = [01ABC]; q2 expected = [01XYZ]; q3 expected = [] (skipped negative).
            s.executeUpdate( "INSERT INTO retrieval_queries VALUES ('core', 'q1', 'cite a wiki page', ARRAY['01ABC'])" );
            s.executeUpdate( "INSERT INTO retrieval_queries VALUES ('core', 'q2', 'write an MCP tool', ARRAY['01XYZ'])" );
            s.executeUpdate( "INSERT INTO retrieval_queries VALUES ('core', 'q3', 'cold fusion', ARRAY[]::VARCHAR ARRAY)" );
        }
        this.dao = new RetrievalQualityDao( ds );
        this.metrics = new RetrievalQualityMetrics();
    }

    @AfterEach
    void tearDown() throws Exception {
        try ( Connection c = ds.getConnection(); Statement s = c.createStatement() ) {
            s.executeUpdate( "DROP TABLE retrieval_runs" );
            s.executeUpdate( "DROP TABLE retrieval_queries" );
            s.executeUpdate( "DROP TABLE retrieval_query_sets" );
        }
    }

    // q1's matching page is "CitingAWikiPage" -> 01ABC; q2 -> "WritingMcpTool" -> 01XYZ.
    private static final Map< String, String > SLUG_TO_ID = Map.of(
        "CitingAWikiPage", "01ABC",
        "WritingMcpTool",  "01XYZ",
        "Random",          "01OTHER"
    );

    private DefaultRetrievalQualityRunner runner( final DefaultRetrievalQualityRunner.Retriever r ) {
        return new DefaultRetrievalQualityRunner(
            dao, r,
            slug -> Optional.ofNullable( SLUG_TO_ID.get( slug ) ),
            metrics );
    }

    @Test
    void runNow_perfectRetrieval_givesNdcgOne() {
        final DefaultRetrievalQualityRunner.Retriever r = ( mode, q ) -> {
            if ( q.contains( "cite" ) )       return List.of( "CitingAWikiPage", "Random" );
            if ( q.contains( "MCP" ) )        return List.of( "WritingMcpTool", "Random" );
            return List.of();
        };
        final RetrievalRunResult result = runner( r ).runNow( "core", RetrievalMode.HYBRID );
        assertEquals( 2, result.queriesEvaluated() );
        assertEquals( 1, result.queriesSkipped() );    // q3 has empty expected
        assertEquals( 1.0, result.ndcgAt5(), 1e-9 );
        assertEquals( 1.0, result.mrr(), 1e-9 );
        assertNotEquals( 0L, result.runId() );
        assertFalse( result.degraded() );
    }

    @Test
    void runNow_emptyRetrieval_givesZero() {
        final DefaultRetrievalQualityRunner.Retriever r = ( mode, q ) -> List.of();
        final RetrievalRunResult result = runner( r ).runNow( "core", RetrievalMode.BM25 );
        assertEquals( 2, result.queriesEvaluated() );
        assertEquals( 0.0, result.ndcgAt5() );
        assertEquals( 0.0, result.mrr() );
    }

    @Test
    void runNow_unknownSet_throws() {
        assertThrows( IllegalArgumentException.class,
            () -> runner( ( m, q ) -> List.of() ).runNow( "no-such-set", RetrievalMode.HYBRID ) );
    }

    @Test
    void runNow_invalidArgs_throw() {
        final DefaultRetrievalQualityRunner run = runner( ( m, q ) -> List.of() );
        assertThrows( IllegalArgumentException.class, () -> run.runNow( null, RetrievalMode.HYBRID ) );
        assertThrows( IllegalArgumentException.class, () -> run.runNow( "core", null ) );
    }

    @Test
    void runNow_retrieverThrows_marksRunDegraded() {
        final AtomicInteger calls = new AtomicInteger();
        final DefaultRetrievalQualityRunner.Retriever r = ( mode, q ) -> {
            if ( calls.incrementAndGet() == 1 ) throw new RuntimeException( "embedder down" );
            return List.of( "WritingMcpTool" );
        };
        final RetrievalRunResult result = runner( r ).runNow( "core", RetrievalMode.HYBRID );
        assertTrue( result.degraded() );
        // q1 fails (degraded), q2 succeeds, q3 skipped.
        assertEquals( 1, result.queriesEvaluated() );
    }

    @Test
    void runNow_slugWithoutCanonicalIdIsDropped() {
        // Retriever returns one un-mappable slug — the resolver returns Optional.empty
        // and the runner treats that as "no predictions", giving nDCG = 0.
        final DefaultRetrievalQualityRunner.Retriever r = ( mode, q ) -> List.of( "PageWithNoCanonicalId" );
        final RetrievalRunResult result = runner( r ).runNow( "core", RetrievalMode.HYBRID );
        assertEquals( 0.0, result.ndcgAt5() );
        assertFalse( result.degraded() );
    }

    @Test
    void recentRuns_delegatesToDao() {
        final RetrievalRunResult r = runner( ( m, q ) -> List.of( "CitingAWikiPage" ) )
            .runNow( "core", RetrievalMode.HYBRID );
        final List< RetrievalRunResult > rows =
            runner( ( m, q ) -> List.of() ).recentRuns( "core", RetrievalMode.HYBRID, 10 );
        assertEquals( 1, rows.size() );
        assertEquals( r.runId(), rows.get( 0 ).runId() );
    }

    @Test
    void scheduleNightly_isIdempotent_andCloseable() {
        final DefaultRetrievalQualityRunner run = runner( ( m, q ) -> List.of() );
        assertDoesNotThrow( run::scheduleNightly );
        assertDoesNotThrow( run::scheduleNightly );  // second call is a no-op
        assertDoesNotThrow( run::close );
        assertDoesNotThrow( run::close );  // close is idempotent too
    }

    @Test
    void runAllForNightly_runsEveryMode_overSeededSet() throws Exception {
        // Seed the set with the runner's nightly id so runAllForNightly exercises it.
        try ( Connection c = ds.getConnection(); Statement s = c.createStatement() ) {
            s.executeUpdate( "INSERT INTO retrieval_query_sets (id, name) VALUES ('core-agent-queries', 'Core')" );
            s.executeUpdate( "INSERT INTO retrieval_queries VALUES ('core-agent-queries', 'q1', 'cite', ARRAY['01ABC'])" );
        }
        final DefaultRetrievalQualityRunner run = runner( ( m, q ) -> List.of( "CitingAWikiPage" ) );
        run.runAllForNightly();
        final List< RetrievalRunResult > rows = dao.recentRuns( "core-agent-queries", null, 100 );
        assertEquals( RetrievalMode.values().length, rows.size() );
    }

    @Test
    void secondsUntilNextRun_picksTomorrowAfterTargetHour() {
        final DefaultRetrievalQualityRunner run = new DefaultRetrievalQualityRunner(
            dao, ( m, q ) -> List.of(), slug -> Optional.empty(), metrics, 3 );
        // 04:00 UTC (after 03:00) -> next run is tomorrow 03:00 -> ~23h.
        final long secs = run.secondsUntilNextRun( Instant.parse( "2026-04-25T04:00:00Z" ) );
        assertTrue( secs > 22 * 3600 && secs < 24 * 3600,
            "expected ~23h, got " + secs + "s" );
    }

    @Test
    void runNow_persistsRowReadableByDao() {
        runner( ( m, q ) -> List.of( "CitingAWikiPage" ) ).runNow( "core", RetrievalMode.HYBRID_GRAPH );
        final List< RetrievalRunResult > rows = dao.recentRuns( "core", RetrievalMode.HYBRID_GRAPH, 10 );
        assertEquals( 1, rows.size() );
        assertNotNull( rows.get( 0 ).startedAt() );
    }

    @Test
    void runNow_emptyExpectedAggregateNullWhenAllSkipped() throws Exception {
        try ( Connection c = ds.getConnection(); Statement s = c.createStatement() ) {
            s.executeUpdate( "INSERT INTO retrieval_query_sets (id, name) VALUES ('all-empty', 'Empty')" );
            s.executeUpdate( "INSERT INTO retrieval_queries VALUES ('all-empty', 'q1', 'foo', ARRAY[]::VARCHAR ARRAY)" );
        }
        final RetrievalRunResult r =
            runner( ( m, q ) -> List.of() ).runNow( "all-empty", RetrievalMode.HYBRID );
        assertEquals( 0, r.queriesEvaluated() );
        assertEquals( 1, r.queriesSkipped() );
        assertNull( r.ndcgAt5() );
        assertNull( r.mrr() );
    }
}
