package com.wikantik.knowledge.eval;

import com.wikantik.api.eval.RetrievalMode;
import com.wikantik.api.eval.RetrievalRunResult;
import org.apache.logging.log4j.LogManager;
import org.apache.logging.log4j.Logger;

import javax.sql.DataSource;
import java.sql.Array;
import java.sql.Connection;
import java.sql.PreparedStatement;
import java.sql.ResultSet;
import java.sql.SQLException;
import java.sql.Statement;
import java.sql.Timestamp;
import java.sql.Types;
import java.time.Instant;
import java.util.ArrayList;
import java.util.LinkedHashSet;
import java.util.List;
import java.util.Set;

/**
 * JDBC access to the retrieval-quality tables ({@code retrieval_query_sets},
 * {@code retrieval_queries}, {@code retrieval_runs}). Schema lives in
 * {@code V016__retrieval_quality.sql}; this DAO handles only data access.
 *
 * <p>Methods are thread-safe; the DAO itself is stateless and reusable.</p>
 */
public final class RetrievalQualityDao {

    private static final Logger LOG = LogManager.getLogger( RetrievalQualityDao.class );

    /** A single (query_text, expected_ids) pair from the seed table. */
    public record EvalQuery( String queryId, String queryText, Set< String > expectedIds ) {}

    private final DataSource dataSource;

    public RetrievalQualityDao( final DataSource dataSource ) {
        if ( dataSource == null ) throw new IllegalArgumentException( "dataSource must not be null" );
        this.dataSource = dataSource;
    }

    /** Whether the named set exists. */
    public boolean querySetExists( final String querySetId ) {
        final String sql = "SELECT 1 FROM retrieval_query_sets WHERE id = ?";
        try ( Connection c = dataSource.getConnection();
              PreparedStatement ps = c.prepareStatement( sql ) ) {
            ps.setString( 1, querySetId );
            try ( ResultSet rs = ps.executeQuery() ) {
                return rs.next();
            }
        } catch ( final SQLException e ) {
            LOG.warn( "querySetExists({}) failed: {}", querySetId, e.getMessage(), e );
            return false;
        }
    }

    /** Load every {@link EvalQuery} for {@code querySetId}, in deterministic order by {@code query_id}. */
    public List< EvalQuery > loadQueries( final String querySetId ) {
        final String sql = "SELECT query_id, query_text, expected_ids "
            + "FROM retrieval_queries WHERE query_set_id = ? ORDER BY query_id";
        final List< EvalQuery > out = new ArrayList<>();
        try ( Connection c = dataSource.getConnection();
              PreparedStatement ps = c.prepareStatement( sql ) ) {
            ps.setString( 1, querySetId );
            try ( ResultSet rs = ps.executeQuery() ) {
                while ( rs.next() ) {
                    final Array arr = rs.getArray( 3 );
                    final Set< String > expected = arrayToSet( arr );
                    out.add( new EvalQuery( rs.getString( 1 ), rs.getString( 2 ), expected ) );
                }
            }
        } catch ( final SQLException e ) {
            LOG.warn( "loadQueries({}) failed: {}", querySetId, e.getMessage(), e );
            return List.of();
        }
        return out;
    }

    /**
     * Persist a {@link RetrievalRunResult}. Returns the {@code run_id} of the
     * inserted row (the input's {@code runId} is ignored — generated by the
     * sequence).
     */
    public long insertRun( final RetrievalRunResult result ) {
        final String sql = "INSERT INTO retrieval_runs "
            + "(query_set_id, started_at, finished_at, mode, "
            + " ndcg_at_5, ndcg_at_10, recall_at_20, mrr) "
            + "VALUES (?, ?, ?, ?, ?, ?, ?, ?)";
        try ( Connection c = dataSource.getConnection();
              PreparedStatement ps = c.prepareStatement( sql, Statement.RETURN_GENERATED_KEYS ) ) {
            ps.setString( 1, result.querySetId() );
            ps.setTimestamp( 2, Timestamp.from( result.startedAt() ) );
            if ( result.finishedAt() == null ) {
                ps.setNull( 3, Types.TIMESTAMP );
            } else {
                ps.setTimestamp( 3, Timestamp.from( result.finishedAt() ) );
            }
            ps.setString( 4, result.mode().wireName() );
            setNullableDouble( ps, 5, result.ndcgAt5() );
            setNullableDouble( ps, 6, result.ndcgAt10() );
            setNullableDouble( ps, 7, result.recallAt20() );
            setNullableDouble( ps, 8, result.mrr() );
            ps.executeUpdate();
            try ( ResultSet keys = ps.getGeneratedKeys() ) {
                return keys.next() ? keys.getLong( 1 ) : 0L;
            }
        } catch ( final SQLException e ) {
            LOG.warn( "insertRun(set={},mode={}) failed: {}",
                result.querySetId(), result.mode().wireName(), e.getMessage(), e );
            return 0L;
        }
    }

    /**
     * Recent runs in reverse-chronological order. {@code querySetId} and
     * {@code mode} are optional filters; pass {@code null} to skip either.
     * {@code limit} is clamped to {@code [1, 1000]}.
     */
    public List< RetrievalRunResult > recentRuns( final String querySetId,
                                                  final RetrievalMode mode,
                                                  final int limit ) {
        final int clamped = Math.max( 1, Math.min( limit, 1000 ) );
        final StringBuilder sql = new StringBuilder(
            "SELECT run_id, query_set_id, started_at, finished_at, mode, "
                + "ndcg_at_5, ndcg_at_10, recall_at_20, mrr FROM retrieval_runs " );
        final List< Object > params = new ArrayList<>();
        boolean haveWhere = false;
        if ( querySetId != null && !querySetId.isBlank() ) {
            sql.append( "WHERE query_set_id = ? " );
            params.add( querySetId );
            haveWhere = true;
        }
        if ( mode != null ) {
            sql.append( haveWhere ? "AND " : "WHERE " ).append( "mode = ? " );
            params.add( mode.wireName() );
        }
        sql.append( "ORDER BY started_at DESC LIMIT ?" );
        params.add( clamped );

        final List< RetrievalRunResult > out = new ArrayList<>();
        try ( Connection c = dataSource.getConnection();
              PreparedStatement ps = c.prepareStatement( sql.toString() ) ) {
            for ( int i = 0; i < params.size(); i++ ) {
                final Object p = params.get( i );
                if ( p instanceof Integer iv ) ps.setInt( i + 1, iv );
                else                            ps.setString( i + 1, (String) p );
            }
            try ( ResultSet rs = ps.executeQuery() ) {
                while ( rs.next() ) {
                    out.add( mapRow( rs ) );
                }
            }
        } catch ( final SQLException e ) {
            LOG.warn( "recentRuns(set={}, mode={}) failed: {}",
                querySetId, mode == null ? "*" : mode.wireName(), e.getMessage(), e );
            return List.of();
        }
        return out;
    }

    // ---- internals ----

    private static RetrievalRunResult mapRow( final ResultSet rs ) throws SQLException {
        final long runId = rs.getLong( 1 );
        final String setId = rs.getString( 2 );
        final Timestamp started  = rs.getTimestamp( 3 );
        final Timestamp finished = rs.getTimestamp( 4 );
        final RetrievalMode mode = RetrievalMode.fromWire( rs.getString( 5 ) )
            .orElse( RetrievalMode.HYBRID );
        return new RetrievalRunResult(
            runId,
            setId,
            mode,
            getNullableDouble( rs, 6 ),
            getNullableDouble( rs, 7 ),
            getNullableDouble( rs, 8 ),
            getNullableDouble( rs, 9 ),
            started == null ? Instant.EPOCH : started.toInstant(),
            finished == null ? null : finished.toInstant(),
            0,
            0,
            false );
    }

    private static void setNullableDouble( final PreparedStatement ps, final int idx,
                                           final Double value ) throws SQLException {
        if ( value == null ) {
            ps.setNull( idx, Types.DECIMAL );
        } else {
            ps.setDouble( idx, value );
        }
    }

    private static Double getNullableDouble( final ResultSet rs, final int idx ) throws SQLException {
        final double v = rs.getDouble( idx );
        return rs.wasNull() ? null : v;
    }

    private static Set< String > arrayToSet( final Array arr ) throws SQLException {
        if ( arr == null ) return Set.of();
        final Object raw = arr.getArray();
        if ( !( raw instanceof Object[] objs ) ) return Set.of();
        final Set< String > out = new LinkedHashSet<>( objs.length );
        for ( final Object o : objs ) {
            if ( o != null ) out.add( o.toString() );
        }
        return out;
    }
}
